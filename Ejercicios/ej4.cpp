//Explicar, desde el punto de vista de C++, las diferencias entre estructuras y clases

/*
La principal y única diferencia entre estructurras y clases, es que las estructuras
poseen su contenido en formato público por defecto, por lo que de ser necesario 
ocultar parte de su contenido requiere colocar "private:" en dicha sección.
Por otra parte las Clases en C++ tienen su contenido por defecto en estado "privado" por lo que
de ser necesario que una parte de la clase sea pública, se debe colocar "public:" en dicha sección.
*/